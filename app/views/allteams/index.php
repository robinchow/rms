    <div class="page" id="band">
      <div class="container">
        <h1>Band</h1>
        <p>The band are the masters of sound and music. As a member you will be part of the atmosphere and intrigue surrounding dances and skits. The band is also given time to shine in the illustrious band number - a highlight of any revue. They also have a great time, with a reputation for partying harder and faster than any that have come before.</p>
        <p>Band puts in a dozen or so rehearsals and the four performance nights in session 2. Outside of that, the band is always looking for ways to avoid work, chill, and jam the night away. If you are an aspiring band member then you will need to bring your own instrument along with the best musical talent you can muster.</p>
      </div>
    </div>
    <div class="cover-pic" id="three"></div>
    <div class="page" id="cast">
      <div class="container">
        <h1>Cast</h1>
        <p>The cast are made up of the actors, singers, and dancers (often all rolled into one) who perform on stage (and in videos) for the show. The cast are charged with power! The power to make the audience laugh, cheer, and lose control of their bowels every night.</p>
        <p>Being a member of cast requires a massive commitment, Life becomes an intense blur of rehearsals, promotions, and performing, with Directors poking you the whole way along (don't ask with what).</p>
        <p>BUT! Being in cast is also arguably the most rewarding and exciting thing that you will ever do with your life ever! Making hundreds of people laugh, getting to spend 3 months with a bunch of awesome people, making friends that will last a lifetime, and making a very special contribution to this crazy ol thing we call CSE Revue.</p>
        <p>Sounds like something you want to do? The wonderful thing about revue is to be in cast NO EXPERIENCE IS REQUIRED! You don't have to be the loudest or funniest person in the world either, we'll teach you all the wicked skills you'll need. All we need from you is your dedication, enthusiasm, and luurve.</p>
      </div>
    </div>
    <div class="cover-pic" id="four"></div>
    <div class="page" id="costumes">
      <div class="container">
        <h1>Costumes and Makeup</h1>
        <p>The Costumes team is responsible for adding that extra li'l oomph on stage, making the cast look their sparkly best, for dressing up the Promotions team with costumes to make people turn and stare, and of course having awesome fun making the rest of Revue look so damn good (oh yeah)! Costumes is a great team to join for helping out behind the scenes with the added bonus that you get to see the products of your creation on stage!</p>
        <p>It doesn't matter if you don't know how to sew. CSE's Costumes Team is a great place for learning some fundamental aspects of theatre costume design, practicing sewing skills, recapturing your youth and going back to those days of glitter-glue and finger-painting.</p>
        <p>If you have a bit of creative flair for designing eye-catching costumes, a weakness for the (oh so sexy) rev of those sewing machines, are an absolute artiste! with a pair of scissors in hand or are just interested in learning and creating things that will turn up on stage then Costumes is the team for you.</p>
      </div>
    </div>
    <div class="cover-pic" id="five"></div>
    <div class="page" id="promos">
      <div class="container">
        <h1>Promotions and Marketing</h1>
        <p>Promotions is the face of the revue. We inform the unexpecting populace of all the cool events and the awesome show that we put on.</p>
        <p>We are responsible for assisting people to see the light and join the society and to see the show! All in all the Promos team's goal is to raise awareness of our fantastic show and bring in both new members and an audience.</p>
        <p>To be a part of Promotions, all you need is enthusiasm and a smile (and we can teach the smile). If you've got a mind for marketing or some good ideas on how to help people notice us, we're always looking for some help on the creative side of things.</p>
        <p>If you want to make CSE Revue a more populous and happy place, or just learn how to communicate better and gain confidence, Promotions is the team for you.</p>
      </div>
    </div>
    <div class="cover-pic" id="six"></div>
    <div class="page" id="scripts">
      <div class="container">
        <h1>Scripts</h1>
        <p>The scripts team is home to the writers, the visionaries, the loud, the quiet, the offensive, the politically correct, the good, the bad, and the directors. And of course, YOU!</p>
        <p>The scripts team pump the show full of goodness in the form of comedy. We play games, talk loudly, brainstorm, discuss ideas, and when the time is write, we right material for the show!</p>
        <p>No previous writing experience is necessary, and you don’t have to be the world’s funniest person to enter! Scripts is a network and community, a giant mish mash of people and ideas from which amazing things always emerge.</p>
        <p>Scripts is a great way to get mixed up in this crazy thing called revue in session 1, and to be involved right from the word go. It’s also a wonderful way to get to know the people in CSE revue before the show starts to gear up. If something you wrote/suggested ends up in show, you can tell/gloat to all your friends about it!</p>
        <p>And if you really, really don’t want to be involved in scripts, please come to a meeting and tell us why not.</p>
      </div>
    </div>
    <div class="cover-pic" id="seven"></div>
    <div class="page" id="social">
      <div class="container">
        <h1>Social</h1>
        <p>Social is the FUN factory of CSE Revue! Like Charlie and the Chocolate Factory we make it so that everyone is incorporated in the ride of Revue, enjoys fun times and takes away an abundance of memories. Being part of the social team means taking care of the little details to make Revue parties and the myriad other events each a raving success. You'll get to meet and greet lots of new and exciting people, and you might pick up some organisational skills on the way.</p>
        <p>If you want to come up with super-cool events to put on or you're just looking to make new friends, you'll fit right into this cosy little team. "But what about the time commitment?" you might ask. Well that's up to you. The Social team is active all year round. So if you want to come up with fun, fresh ideas for social activities, the not-so-secret password is just a smile. Hyperactivity is optional.</p>
      </div>
    </div>
    <div class="cover-pic" id="eight"></div>
    <div class="page" id="vfx">
      <div class="container">
        <h1>VFX</h1>
        <p>Take a moment in time when you giggle profusely at something very silly. What would happen when if you were to replay that moment, over and over again? Slower and faster? Backwards and forwards? And what would happen when a group of mates were sitting around, reveling in the silliness, falling over each other laughing like hyenas?</p>
        <p>What you've just read is a description of scrubbing, just one of hundreds of tools available to you as an editor. And editing is just one part of making videos, each as fun as the last. There's storyboarding, shooting, animation, compositing, colour correction and more.</p>
        <p>The VFX team takes ideas and scripts that are too hard to do on stage (or can be done much better through a lens) and uses professional equipment to make them come to life on the screen.</p>
        <p>We'll teach you everything you need to know about film making, but if you're a photographer, 3D animator or if you've just seen far too many movies, we'd love to have you on board.</p>
      </div>
    </div>
    <div class="cover-pic" id="nine"></div>
    <div class="page" id="webmin">
      <div class="container">
        <h1>Webmin</h1>
        <p>Ever wanted to take part in a challenging and exciting technical projects? Whether or not you possess technical skills, the webmin is for you.</p>
        <p>The webmin (Website and administration) team is responsible for designing, developing and maintaining the CSE Revue website and mailing lists, as well as the related ticketing system which has been used in a number of on campus productions. As well as this we are in charge of the technical administration behind CSE Revue.</p>
        <p>Even if you don't have any programming skill, you can still be of valuable contribution in design (both art & user interface), feature ideas, and content contribution. That said, we also run lessons for those interested in learning how to make dynamic database driven websites.</p>
        <p>The following skills are useful if you are considering joining (but we teach you as well!):</p>
        <ul>
          <li>Willingness to learn</li>
          <li>HTML</li>
          <li>Javascript</li>
          <li>PHP</li>
          <li>Scripting skills (esp. Perl)</li>
          <li>Design (on paper, or using design programs like Illustrator and Photoshop)</li>
          <li>System administration</li>
        </ul>
      </div>
