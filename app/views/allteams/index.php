        <img src="http://www.cserevue.org.au/img/logo.png" alt=""><h1>CSE Revue</h1>
        <p class="lead">The Computer Science and Engineering (CSE) Revue is a live comedy sketch show held during September at the University of New South Wales (UNSW). Produced and directed by members of the society, the show serves to highlight the technical and creative talents of UNSW students, as well as an opportunity for students to further develop their university experience.</p>
