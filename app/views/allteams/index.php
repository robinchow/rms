        <h1>Front of House and Sales</h1>
        <p>Front of House (a.k.a FoH) is all about keeping the audience happy. FoH make sure they are entertained before the show begins, provide them with food and drink, give them a band to listen to and show them to their seats. FoH ensure the audience come away from an enjoyable experience all around. FoH members enjoy socialising, talking and making people happy. FoH also attracts people with an eye for detail - the decorators among you will be responsible for the theatre decorations.</p>
        <p>The Front of House Team will also be involved in filling the showbags we give to our audience with goodies. This means finding cool free things to hand out...everyone loves free stuff!</p>
        <p>You can also join other teams that don't operate during show nights - that means being part of the whole CSE Revue experience without giving your time entirely to one team. And the big bonus? The chance to see the show for free. Yes, that's right, free!</p>
