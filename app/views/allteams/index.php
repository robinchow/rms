      <div class="container">
        <h1>Wellbeing</h1>
        <p>Wellbeing is all about food and health!</p>
        <p>Delicious, life nurturing food and general health.</p>
        <p>As a member of the kind Wellbeing team, you'll be looking after the cast and crew involved in the show by assisting a team to cook for forty humans! hahaha, just kidding. Try a hundred and forty.</p>
        <p>Don't think you'll need cooking talents in any sense (but if you have them - awesome! you can teach us a thing or two).</p>
        <p>We guarantee a hilarious time experimenting with recipes, watching kitchens being reduced to ashes, and having a celebratory shingdig when something edible happens to pop out of the stove!</p>
        <p>All we need is a few of your hours in the evening whenever you can spare them, whether you're going dice happy with the meat and vege, teaching/learning a new recipe, or if you think moral support's your strong suit.</p>
        <p>Wellbeing's also one of the more casual teams in terms of time commitment, so help out on any nights that you'd like. But you should know, the more often you appear through the doors, carrying hot, mouth watering food, the more everyone involved in the show will love you.</p>
        <p>Don't think that we only cook during Show and Production Weeks!</p>
        <p>We also have social Cooking sessions, and the occassional Cooking class, but most of all, we also look after camp! But it's not as if you're there to cook for everyone, you'll be at camp not only for the social activities, but also as a team member that facilitates Revue campers cook everyones meals on a rotational basis.</p>
      </div>
    </div>
    <div class="cover-pic" id="eleven"></div>
    <div class="page" id="fx">
      <div class="container">
        <h1>FX</h1>
        <p>Once the show is surrounded by the lights, sounds, sets and props that Tech provide, it grows to a whole new level. The Special FX team take this one step further, by providing special technical costumes, technical props and generally making things look cool.</p>
        <p>If you can wield a soldering iron with the precision of a surgeon or can put together electrical circuits in the blink of an eye, or if you want to learn how to do these sorts of things, then this is the team for you.</p>
        <p>As Computing and Engineering students, CSE Revue has a reputation to uphold for making things light up, get dark or just go boom and as a member of the Special FX team, you'll be working to make this reputation better than ever. We can teach all the skills required, so if you're new to making things but would like to be involved you're welcome.</p>
      </div>
    </div>
    <div class="cover-pic" id="twelve"></div>
    <div class="page" id="dubs">
      <div class="container">
        <h1>Design and Publications</h1>
          <p>Since 2014, the Design team and Publications team have been merged to create an <b>Ultimate Unit</b> proficient enough to handle the challenges thrown our way. As members of the Dubs team, we specialise in designing and producing creative content for CSE Revue.</p>
          <p>Our main responsibilities include:</p>
          <ul>
            <li>Keeping track of what's going on in the society (and making sure that <i>everyone knows</i> of what’s going on in the society)</li>
            <li>Planning/ producing programmes and newsletters</li>
            <li>Producing promotional products</li>
            <li>Designing websites, T-shirts, stage set, etc.</li>
            <li>Helping promote CSE’s image to present to <i>the world</i> (ie. our members, audience, sponsors, and many random passers-by)</li>
          </ul>
          <p>To join, you <b><i>do not</i></b> need prior skills or experience (although they are most welcome). Having an artistic edge is always a plus, but you'll learn how to use the tools of the trade, along with some theory behind effective visual design, throughout the year.</p>
          <p>So if you’re one to draw and design graphics, document memorable events (either by writing about it, or photographing on the day), or simply just see your ideas come to life, then you’re perfect for the job! We would like to invite you on board to take part in some of the innovative makings of CSE Revue.</p>
      </div>
