    <div class="page" id="tech">
      <div class="container">
        <h1>Tech</h1>
        <p>The Tech team construct the props and the sets for the show, from massive spinning fans, to doors and giant pieces of a monopoly set. During the show Techies operate lights and sound, run around like cockroaches setting the stage and play with the fog machine.</p>
        <p>The only skills necessary are a good appreciation of teamwork and enjoyment of hanging around with others. If you have any experience with lighting, sound, set construction and related skills you'd be a particularly welcome member of the team. Tech consumes about 8 hours a week for the six weeks before show, and up to every show night depending on your interest. But hey, paint doesn't dry very well in the dark, so they'll knock off at sun down and hit the pub.</p>
        <p>Tech is a great place to pick up some fantastic skills, working in the dark with a team of people, potentially making  split-second decisions which directly effect the show each night. You'll learn some painting and construction alongside the in's and outs of theatre. Although you might not operate a light/sound/video console for your first show you'll have the opportunity to watch and assist others in this task.</p>
      </div>
